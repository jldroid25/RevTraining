/**
 * VariableDeclaration
 */
public class VariableDeclaration {

    public static void main(String[] args) {
        System.out.println("-----------------------");
        System.out.println("\n\n");

       // boolean on = true;
        boolean on = false;
        System.out.println("Boolean Datatype: " + on);

        short s = 16;
        System.out.println("Short Datatype: " + s);

        int i = -32;
        System.out.println("Integer Datatype: " + i);

        float f = 3839.34839f;
        System.out.println("Float Datatype: " + f);

        double d = 3839.34839;
        System.out.println("Double Datatype: " + d);

        long l = 4294967296L;
        System.out.println("Long Datatype: " + l);

        char c = 'a';
        System.out.println("Char Datatype: " + c);


        System.out.println("\n");

    }
}